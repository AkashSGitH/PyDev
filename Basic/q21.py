# Demonstrate Bitwise Operations: Write a program to illustrate all the bitwise operations available in Python.

n = int(input("Enter a number: "))
m = int(input("Enter another number: "))

print(f"{n} & {m} = {n & m}")
print(f"{n} | {m} = {n | m}")
print(f"{n} ^ {m} = {n ^ m}")
print(f"~{n} = {~n}")
print(f"{n} << 1 = {n << 1}")
print(f"{n} >> 1 = {n >> 1}")
print(f"{m} << 1 = {m << 1}")
print(f"{m} >> 1 = {m >> 1}")

